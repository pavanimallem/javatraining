package com.hcl.lms;

import java.util.Date;

public class Lms {
	private String empName;
	private long empPhone;
	private String empEmail;
	private String empDept;
	private Date empJoiningdate;
	private int empLeavebal;
	public String getEmpName() {
		return empName;
	}
	public void setEmpName(String empName) {
		this.empName = empName;
	}
	public long getEmpPhone() {
		return empPhone;
	}
	public void setEmpPhone(long empPhone) {
		this.empPhone = empPhone;
	}
	public String getEmpEmail() {
		return empEmail;
	}
	public void setEmpEmail(String empEmail) {
		this.empEmail = empEmail;
	}
	public String getEmpDept() {
		return empDept;
	}
	public void setEmpDept(String empDept) {
		this.empDept = empDept;
	}
	public Date getEmpJoiningdate() {
		return empJoiningdate;
	}
	public void setEmpJoiningdate(Date empJoiningdate) {
		this.empJoiningdate = empJoiningdate;
	}
	public int getEmpLeavebal() {
		return empLeavebal;
	}
	public void setEmpLeavebal(int empLeavebal) {
		this.empLeavebal = empLeavebal;
	}
	

}
